print("Fahrenheit to Celsius Converter Program\n\nFormula used : C = (F - 35) * 5 / 9")
temp = eval(input("\nEnter temperature in Fahrenheit : "))
cel = temp - 32
cel2 = cel * 5
cel3 = cel2 / 9
cel4 = round(cel3, 2) 
print("The Conversion of",temp ," degrees Fahrenheit is ",cel4 , "degrees Celsius.")
